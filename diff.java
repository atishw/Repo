public class Subtraction {
    public static int subtract(int a, int b) {
        return a - b;
    }

    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 5;
        int result = subtract(num1, num2);
        System.out.println("The result of subtraction is: " + result);
    }
}